from util import Stack, Queue  # These may come in handy

class Graph:
    """Represent a graph as a dictionary of vertices mapping labels to edges."""
    def __init__(self):
        self.vertices = {}
    def add_vertex(self, vertex):
        self.vertices[vertex] = set()

    def add_edge(self, v1, v2):
        self.vertices[v1].add(v2)

    def bft(self, starting_vertex):
        visited = [False]*(len(self.vertices)+1)
        queue = []
        queue.append(starting_vertex)
        visited[starting_vertex] = True

        while queue:
            starting_vertex = queue.pop(0)
            print(starting_vertex, end = " ")
            for i in self.vertices[starting_vertex]:
                if visited[i] == False:
                    queue.append(i)
                    visited[i] = True

    def dft(self, starting_vertex, visited=None):
        visited = [False]*(len(self.vertices)+1)
        stack = []
        stack.append(starting_vertex)

        while stack:
            starting_vertex = stack[-1]
            stack.pop()
            if (not visited[starting_vertex]):
                print(starting_vertex, end=" ")
                visited[starting_vertex] = True
            for node in self.vertices.get(starting_vertex):
                if (not visited[node]):
                    stack.append(node)

    def dft_recursiveHelper(self, starting_vertex, visited):
        visited[starting_vertex] = True
        print(starting_vertex, end=" ")

        for i in self.vertices[starting_vertex]:
            if visited[i] == False:
                self.dft_recursiveHelper(i, visited)

    def dft_recursive(self,starting_vertex):  
        visited = [False]*(len(self.vertices)+1) 
        self.dft_recursiveHelper(starting_vertex,visited)

    def dft_recursive_no_helper(self, starting_vertex, visited = None):
        if visited == None:
            visited = []
        visited.append(starting_vertex)
        for vert in self.vertices[starting_vertex]:
            if vert not in visited:
                self.dft_recursive_no_helper(vert, visited)

        return visited

    def bfs(self, starting_vertex, destination_vertex):
        q = Queue()
        q.enqueue([starting_vertex])
        visited = set()

        while q.size() > 0:
            path = q.dequeue()
            v = path[-1]
            if v not in visited:
                if v == destination_vertex:
                    return path
                else:
                    visited.add(v)
                for next_vert in self.vertices[v]:
                    new_path = list(path)
                    new_path.append(next_vert)
                    q.enqueue(new_path)
        return None

    def dfs(self, starting_vertex, destination_vertex):
        s = Stack()
        s.push([starting_vertex])
        visited = set()

        while s.size() > 0:
            path = s.pop()
            v = path[-1]
            if v not in visited:
                if v == destination_vertex:
                    return path
                visited.add(v)
                for next_vert in self.vertices[v]:
                    new_path = list(path)
                    new_path.append(next_vert)
                    s.push(new_path)
        return None






