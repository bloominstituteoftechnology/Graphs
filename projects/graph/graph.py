        queue = Queue()
        queue.enqueue(starting_vertex)

        while queue.size() !=0:
            vertex_id = queue.dequeue()
            neighbors = self.vertices[vertex_id]

            for neighbor_id in neighbors:
                if neighbor_id is havntVisited:
                    havntVisited.remove(neighbor_id)
                    queue.enqueue(neighbor_id)
